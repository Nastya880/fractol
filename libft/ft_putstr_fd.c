#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	i;

	i = 0;
	if (fd != -1)
	{
		if (!s)
			write(1, "(null)", 6);
		else
		{
			while (s[i])
			{
				write(1, &s[i], 1);
				i++;
			}
		}
	}
}
